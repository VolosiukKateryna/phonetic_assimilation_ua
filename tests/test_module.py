import pytest
from modulephon import apply_assimilation, check_for_soft_sounds

# --- 1. Слова для оглушення/одзвінчення (перший блок) ---
block1_words = [
    "легкість", "полегкість", "анекдот", "вокзал", "Вишгород", "Ужгород",
    "зцукром", "безцукру", "зробити", "зшити", "дьогтю", "зцементувати",
    "зсипати", "стверджувати", "осьде", "просьба", "боротьба", "плацдарм",
    "плацкарт", "нігтичком", "лічба", "футбол", "баскетбол", "бейсбол",
    "отже", "зсередини", "зчепити", "айсберг", "бджола", "зносити",
    "цвіркун", "зцілити", "Жмеринка", "вогкість", "вогке"
]

@pytest.mark.parametrize("word", block1_words)
def test_block1_assimilation(word):
    result = apply_assimilation(word)
    assert isinstance(result, str)
    assert len(result) > 0


